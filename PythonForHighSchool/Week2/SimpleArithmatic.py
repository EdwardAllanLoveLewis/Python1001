# Addition (+)
a = 1 + 2
print(a)

# subtraction(-)
b = 2 - 1
print(b)

# Multiplication(*)
c = 2 * 3
print(c)

# division
d = 4 / 2
print(d)

e = 3 / 4
print(e)

# powers
f = 2 ** 3  # Will store 2 to the power of 3 in the variable f
print(f)

# How can we perform square roots or any type of root?

g = 4 ** 0.5
print(g)
# Will print 4 to the power of a half or sqrt(4)
